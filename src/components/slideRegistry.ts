export function resolveSlides(order: string[]): SlideDef[] {
  return order
    .map((id) => SLIDE_COMPONENTS[id])
    .filter((s): s is SlideDef => s != null);
}

export function getAllSlideIds(): string[] {
  return Object.keys(SLIDE_COMPONENTS);
}

